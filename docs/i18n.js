const LANG_MAP = { Español: 'es', English: 'en', Português: 'pt' };

function getLang() {
  if (typeof window !== 'undefined' && window.__enebaLang) return window.__enebaLang;
  return 'es';
}

function t(key) {
  const parts = key.split('.');
  let obj = I18N[getLang()] || I18N.es;
  for (const p of parts) {
    obj = obj?.[p];
    if (obj === undefined) {
      obj = I18N.es;
      for (const q of parts) obj = obj?.[q];
      break;
    }
  }
  return typeof obj === 'string' ? obj : key;
}

function applyDocumentLang(lang) {
  const htmlLang = { es: 'es-CO', en: 'en-US', pt: 'pt-BR' };
  document.documentElement.lang = htmlLang[lang] || 'es';
}
